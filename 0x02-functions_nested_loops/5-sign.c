#include "main.h"

/**
 * print_sign-> display positive or negative
 * @n: argument for intiger
 * Return: negative or posative
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
